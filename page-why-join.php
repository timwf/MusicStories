            <li>Music teachers can create their own “shop window” and set up products or services for people to book directly on site, e.g., lessons, online performances, group classes, other services online or in person.</li>
            <li>Booking, payment, cancellations and diary management are integrated and easy. </li>
            <li>Users are part of the Music Stories Community and benefit from all areas of the site and the network.</li>
            <li>We build relevant content around the musicians, with everything in one place to help people create their musical stories.</li>
