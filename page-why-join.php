        <h2>What is Music Stories?</h2>
        <p>Music Stories was born from our own experience as amateur musicians, struggling to find a music teacher online. We struggled to find a good website which presented music teachers well, helped you choose between music teachers or helped develop our musical interests.</p>
        <p>Music Stories brings together different elements of the music industry; “Learn”, “Play”, “Experience” and “Shop” and creates a space designed for musicians by musicians.</p>
        <ul>
            <li><strong>Learn:</strong> find and book a music teacher directly on site</li>
            <li><strong>Play:</strong> connect and network with other musicians</li>
            <li><strong>Experience:</strong> find musical performances, masterclasses, music weekends and other online experiences</li>
            <li><strong>Shop:</strong> find your nearest music shop or shop online for everything music</li>
        </ul>
        <p><strong>Benefits for the music teacher and / or performer.</strong></p>
        <ul>
            <li>A marketing platform to sell online lessons, in person lessons, masterclasses, online performances, or other music services; more bookings for you.</li>
            <li>A booking and payment system to make it easier for you.</li>
            <li>Join the Music Stories community and network with other musicians!</li>
            <li>Benefit from offers from independent retailers and other music services.</li>
            </ul>
            <p><strong>Benefits for the musician.</strong></p>
        <ul>
            <li>Easily find and book music lessons </li>
            <li>Find gifts for people such as online musical performances or masterclasses or an introduction to an instrument.</li>
            <li>Join the Music Stories community and network with other musicians!</li>
            <li>Benefit from offers from independent retailers and other music services.</li>
